	<div class="section">
		<h2>About WiCHacks</h2>

		<p>WiCHacks is an all women 24-hour hackathon hosted by Women in Computing at RIT. A <b>hackathon</b> is a collaborative programming event in which participants create an app, website, game, or other piece of software over the course of the event.  Hackathon participants can be female college (including non-RIT) or high school students</b> who are at least 16 years of age.</p>

		<p><b>This event is open to women of all skill levels</b>: those who haven’t programmed at all, women who have done some programming, and the best women programmers out there. It will be a great day to learn, invent, and create the future.  Don’t have a team in mind?  No worries -- RIT Women in Computing will be hosting a series of events (including one on the morning of the event) to facilitate team formation.  <b>All meals (breakfast, lunch, dinner, and snacks) will be provided, and participation is free.</b></p>

		<p>New to programming? Join our <a href="#newbie"><strong>Newbie Track!</strong></a> The Newbie Track runs right along with the hackathon and teaches basic programming skills to those who have little to no programming experience.</p>

   <p>All hackers and newbies who participate in the hackathon will have the opportunity to upload their resume to our resume database that will be shared with all of our sponsors!</p>
	</div>

 <div class="divider" id="newbie"></div>
 <div class="section">
       <h2>Newbie Track</h2>

       <p>A Newbie is a future programmer who wants to learn about coding and acquire the basics of a programming language. This track is for students who want to be involved in the hackathon but have no programming experience. This will give students the opportunity to learn how to code and produce a project over the course of the hackathon. The Newbie Track will cover Web Development featuring HTML, CSS, and Javascript!</p>
 </div>

